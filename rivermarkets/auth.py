"""Ed25519 request signing for the River Markets API.

Hand-written — not generated by Fern. Do not delete on regen.
See https://docs.rivermarkets.com/api-reference/authentication for the
canonical-string format and header contract.
"""

import base64
import hashlib
import time
import typing
from urllib.parse import parse_qsl, quote, urlencode

import httpx
from nacl.signing import SigningKey


class RiverMarketsSigner(httpx.Auth):
    requires_request_body = True

    def __init__(self, key_id: str, private_key: str):
        self._key_id = key_id
        self._signing_key = SigningKey(base64.b64decode(private_key))

    def auth_flow(
        self, request: httpx.Request
    ) -> typing.Generator[httpx.Request, httpx.Response, None]:
        self._sign(request)
        yield request

    def _sign(self, request: httpx.Request) -> None:
        url = request.url
        sorted_query = urlencode(
            sorted(parse_qsl(url.query.decode("ascii"), keep_blank_values=True)),
            quote_via=quote,
        )
        body = request.content or b""
        ts = str(int(time.time()))
        canonical = "\n".join(
            [
                request.method.upper(),
                url.path,
                sorted_query,
                ts,
                hashlib.sha256(body).hexdigest(),
            ]
        ).encode()
        signature = self._signing_key.sign(canonical).signature
        request.headers["X-River-Key-Id"] = self._key_id
        request.headers["X-River-Timestamp"] = ts
        request.headers["X-River-Signature"] = base64.b64encode(signature).decode()
